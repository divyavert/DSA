package com.company.college;

public class rectangle {
    float a;
    float b;
    float c;
    float d;

    public rectangle(int a, int b, int c, int d) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }


    public float area(){
        return a*b;
    }


}
